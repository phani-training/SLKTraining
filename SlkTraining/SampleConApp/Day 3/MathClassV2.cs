using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace SampleConApp.Day_3
{
    class MathClassV2
    {
        public static double AddFunction(double firstValue, double secondValue)
        {
            return firstValue + secondValue;
        }

        public static double SubFunction(double firstValue, double secondValue)
        {
            return firstValue - secondValue;
        }
        public static double MulFunction(double firstValue, double secondValue)
        {
            return firstValue * secondValue;
        }
        public static double DivFunction(double firstValue, double secondValue)
        {
            return firstValue / secondValue;
        }
    }
}
